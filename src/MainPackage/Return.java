    private void midboxKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_midboxKeyPressed

        String member_id = midbox.getText();
        int key = evt.getKeyCode();
        if (key == 10) {
            try {
                String q = "SELECT member_name FROM issue WHERE member_id='" + member_id + "'";
                pst = con.prepareStatement(q);
                rs = pst.executeQuery();
                rs.next();
                name.setText(rs.getString("member_name"));

                PreparedStatement pst1 = con.prepareStatement("SELECT id,risk FROM members WHERE member_id='" + member_id + "'");
                ResultSet rs1 = pst1.executeQuery();
                rs1.next();
                nic.setText(rs1.getString("id"));
                risk.setText(rs1.getString("risk"));
                membercheck.setVisible(true);
                bidbox.requestFocus();

            } catch (Exception e) {
                name.setText("");
                nic.setText("");
                risk.setText("");
                membercheck.setSelected(false);
                membercheck.setVisible(false);
            }
        }
    }//GEN-LAST:event_midboxKeyPressed

