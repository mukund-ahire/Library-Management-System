    private void returnboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnboxActionPerformed

        String member_id = midbox.getText();
        String book_id = bidbox.getText();
        String title = titlebox.getText();
        String isbn = isbnbox.getText();
        String today = datebox.getText();
        String issuedate = issuedatebox.getText();
        String returndate = returndatebox.getText();

        if (membercheck.isSelected() && bookcheck.isSelected()) {
            int x = JOptionPane.showConfirmDialog(null, "CONFIRM RETURN");
            if (x == 0) {

                try {

                    pst = con.prepareStatement("DELETE FROM issue WHERE book_id='" + book_id + "' AND member_id='" + member_id + "'");
                    pst.execute();

                    PreparedStatement pst1 = con.prepareStatement("INSERT INTO availablility VALUES ('" + book_id + "','" + isbn + "','" + title + "')");
                    pst1.execute();

                    PreparedStatement pst2 = con.prepareStatement("INSERT INTO returntable VALUES ('" + book_id + "','" + member_id + "','" + today + "','" + issuedate + "','" + returndate + "')");
                    pst2.execute();
                    tableload();
                    reset();

                } catch (Exception e) {
                    massege.setText("Something Went Wrong");
                }

            }
        } else {
            massege.setText("Validation Failed");
        }
    }//GEN-LAST:event_returnboxActionPerformed

